first = input("Enter the first no.");
operator = input("Enter the operator");
second = input("Enter the second no.");
first = int(first);
second = int(second);

if(operator== '+'):
    print(first+second);

elif(operator== '-'):
    print(first - second);

elif(operator== '*'):
    print(first * second);

elif(operator== '/'):
    print(first / second);

elif(operator== '%'):
    print(first % second);

else:
    print("Please Enter correct operator");